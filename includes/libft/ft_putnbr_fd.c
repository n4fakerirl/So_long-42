#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long	nbr;
	char		result;

	nbr = n;
	if (nbr < 0)
	{
		ft_putchar_fd('-', fd);
		nbr *= -1;
	}
	if (nbr > 9)
	{
		ft_putnbr_fd(nbr / 10, fd);
		ft_putnbr_fd(nbr % 10, fd);
	}
	if (nbr >= 0 && nbr <= 9)
	{
		result = nbr + '0';
		ft_putchar_fd(result, fd);
	}
}
