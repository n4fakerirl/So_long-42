#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		srclen;
	char	*dest;

	i = 0;
	srclen = ft_strlen(s);
	dest = malloc(sizeof(char) * (srclen + 1));
	if (!dest)
		return (NULL);
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
